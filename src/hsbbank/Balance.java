    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            
            con.setAutoCommit(false);
            String accno = txtaccno.getText();
            String cust_id = jLabel7.getText();
            String firstname = jLabel8.getText();
            String lastname = jLabel9.getText();
            String date = jLabel10.getText();
            String balance = jLabel13.getText();
            String amount = txtamount.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/hsbank2","root","");
            insert = con.prepareStatement("insert into deposit(`acc_id`, `cust_id`, `date`, `balance`, `deposit`) values (?,?,?,?,?)");
            insert.setString(1, accno);
            insert.setString(2, cust_id);
            insert.setString(3, date);
            insert.setString(4, balance);
            insert.setString(5, amount);

            insert.executeUpdate();
            
            update = con.prepareStatement("update account set balance = balance+ ? where acc_id = ?");
            update.setString(1, amount);
            update.setString(2, accno);
            
            update.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Deposit...!");
            con.commit();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Balance.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            
            try {
                con.rollback();
            } catch (SQLException ex1) {
                Logger.getLogger(Balance.class.getName()).log(Level.SEVERE, null, ex1);
            }
            Logger.getLogger(Balance.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


